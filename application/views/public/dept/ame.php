								<p>
									Department of Apparel Manufacturing Engineering got down to its journey along with that of School of Applied Science in 1992, the time from which till today it heaves the strength with non-breaking waves. This department has brought numerous graduates into reality that are speeding up not only Bangladesh but also the whole world in various IT fields by their skill that they have gathered from this department. AME department mainly offers four-year undergraduate course in Apparel Manufacturing Engineering where courses are designed in such a way so that students graduating from this department have a balance of theory and practical skills to prepare them for the highly competitive workplace. Now a team of fulltime faculty members and staffs are working ceaselessly under the smashing leadership of Prof. Mohammad Reza Selim, Head, Dept. of AME, beneath his eye of perfection and strictness. Besides four year undergraduate program this department is also successfully running some other activities like Post-graduate Diploma in Information Technology (one year program financed by the ministry of Science, Information & Communication Technology), Certificate in Computer Application (two term program jointly operated with computer center), CISCO Certified Network Program (four term one year program in collaboration with UNDP and CISCO), DCSA program operated jointly with Bangladesh Open University. Department has successfully run some research activities in collaboration with different universities of home & abroad. Some of them are Bangla Computerization, Natural Language Processing, Optical Character Recognition, Parallel Processing, Cluster Computer, CodeWitz, Asia Link Project, and Cellular Phone & Computer Interfacing etc. It has some sole student activities like Participation in ACM Regional and National Programming Competition, Arranging Computer Fair, Publication of Departmental Research Journal etc. Software House is departments another dazzling side whose aspire is to provide the students a professional working environment, which will sharpen their skill in software development. It was first started with 7 students and some young energetic teachers but now many students with highly motivated teachers are working incessantly to put it in pinnacle among all software development companies.
