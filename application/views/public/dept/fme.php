				   				<div class="wrapper-card-right-body">
									<p>
										The Department of Apparel Manufacturing Engineering (AME) offers a comprehensive streamlined curriculum at undergraduate level, which needs 160 credit hours including 4 credit hours in thesis and project work to be completed for achieving bachelor degree. The well thought and full-fledged curriculum allows students greater flexibility and provides experiential learning. This program begins with core courses and ends with specialization in a particular area through thesis and project work under a faculty supervisor.
										<br><br>
										During the first two years, students take compulsory courses in the core areas of Apparel Manufacturing Engineering (AME) as well as in mathematics and other engineering disciplines. In the third and fourth year, the students concentrate in a number of advanced courses in Apparel Manufacturing Engineering (AME) providing deeper and applied knowledge and greater ability to take part in the development of different computer system hardware and software. The syllabus of the department updates regularly with recent development of knowledge and technology in the relevant field of study.
										<br><br>
										The current syllabus has  updated in 2017 and effective for the session 2017-18 and onward. The major inclusion in the updated curriculum is the introduction of various software development projects in second and third year. The minimum CGPA requirement for being awarded of Bachelor of Science degree in Apparel Manufacturing Engineering (AME) is 2.25 out of 4.00.

